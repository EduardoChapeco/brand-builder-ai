    ('video-studio-generation-squad', 'video_prompt_composer', 1, 'Prompt Composer', NULL),
    ('video-studio-generation-squad', 'layer_orchestrator', 2, 'Layer Plan', 'video_prompt_composer'),
    ('video-studio-generation-squad', 'remotion_renderer', 3, 'Render Pipeline', 'layer_orchestrator'),
