INSERT INTO public.squad_template_agents (
  squad_template_id,
  agent_id,
  task_order,
  role_label,
  step_kind,
  depends_on_agent_id,
  is_required,
  input_contract,
  output_contract,
  checkpoint_policy
)
SELECT
  template.id,
  agent.agent_id,
  agent.task_order,
  agent.role_label,
  'task',
  agent.depends_on_agent_id,
  TRUE,
  '{}'::jsonb,
  '{}'::jsonb,
  '{"mode":"manual_review"}'::jsonb
FROM public.squad_templates template
JOIN (
  VALUES
    ('video-studio-editor-squad', 'video_editing_orchestrator', 1, 'Video Edit Lead', NULL),
    ('video-studio-editor-squad', 'audio_analysis_agent', 2, 'Audio Analysis', 'video_editing_orchestrator'),
